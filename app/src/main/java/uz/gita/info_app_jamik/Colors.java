package uz.gita.info_app_jamik;

public class Colors {

    private int color_back;
    private int color_tool;

    public Colors(int color_back, int color_tool) {
        this.color_back = color_back;
        this.color_tool = color_tool;
    }

    public int getColor_back() {
        return color_back;
    }

    public int getColor_tool() {
        return color_tool;
    }
}
